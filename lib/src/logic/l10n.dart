  static String timeAgo(Duration diff) {
    if (diff.inSeconds < 60) return _t('time_just_now');

    if (diff.inMinutes < 60) {
      final n = diff.inMinutes;
      return n == 1
          ? _t('time_one_minute')
          : _t('time_minutes').replaceFirst('{n}', '$n');
    }

    if (diff.inHours < 24) {
      final n = diff.inHours;
      return n == 1
          ? _t('time_one_hour')
          : _t('time_hours').replaceFirst('{n}', '$n');
    }

    if (diff.inDays < 30) {
      final n = diff.inDays;
      return n == 1
          ? _t('time_one_day')
          : _t('time_days').replaceFirst('{n}', '$n');
    }

    if (diff.inDays < 365) {
      final n = diff.inDays ~/ 30;
      return n == 1
          ? _t('time_one_month')
          : _t('time_months').replaceFirst('{n}', '$n');
    }

    final n = diff.inDays ~/ 365;
    return n == 1
        ? _t('time_one_year')
        : _t('time_years').replaceFirst('{n}', '$n');
  }
